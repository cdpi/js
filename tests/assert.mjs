
import { AssertError } from "../assert.mjs";
import * as assert from "../assert.mjs";

/////////////////////////////////////////////////////////////////////////////////////////////////////////////

assert.isInstanceOf(new AssertError(), Error, "isInstanceOf")

/////////////////////////////////////////////////////////////////////////////////////////////////////////////

assert.isNull(null);

assert.throwError(() => assert.isNull(undefined));
assert.throwError(() => assert.isNull(5));
assert.throwError(() => assert.isNull(""));
assert.throwError(() => assert.isNull([]));
assert.throwError(() => assert.isNull({}));

assert.isNotNull(undefined);
assert.isNotNull(5);
assert.isNotNull("");
assert.isNotNull([]);
assert.isNotNull({});

/////////////////////////////////////////////////////////////////////////////////////////////////////////////

/////////////////////////////////////////////////////////////////////////////////////////////////////////////

assert.isSet(5);
assert.isSet("");
assert.isSet([]);
assert.isSet({});

/////////////////////////////////////////////////////////////////////////////////////////////////////////////

assert.isNumber(5);
assert.isNumber(5n);

/////////////////////////////////////////////////////////////////////////////////////////////////////////////

assert.isArray([]);

assert.arrayLength([1, 2, 3, 4, 5], 5);

/////////////////////////////////////////////////////////////////////////////////////////////////////////////

assert.isObject(new AssertError());

assert.isObject({});

assert.objectKeys({a: 1, bc: undefined, z: null}, ["a", "z", "bc"]);
